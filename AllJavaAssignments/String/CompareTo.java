package com.stringassignment;

public class CompareTo {
	
	public static void main(String args[]) {
		String name1 = "lavi";
		String name2 = "Lavi";
		String name3 = "jain";

		System.out.println(name1.compareTo(name2));           // 0 because both are equal
		System.out.println(name1.compareTo(name3));           
	}

}
